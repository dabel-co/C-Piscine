#include "library.h"

void	dec(unsigned int a)
{
	if (a > 99)
	{
		ft_write(a / 100);
		ft_print("100");
	}
	if ((a % 100) > 19)
	{
		ft_write(((a % 100) / 10) * 10);
		if ((a % 10) > 0)
			ft_write(a % 10);
	}
	else if ((a % 100) > 9)
		ft_write(a % 100);
	else if ((a % 10) > 0)
		ft_write(a % 10);
}

void	ft_chop(unsigned int a)
{
	if (a > 999999999)
	{
		dec((a / 1000000000));
		ft_print("1000000000");
	}
	if (a > 999999)
	{
		dec(((a % 1000000000) / 1000000));
		if (((a % 1000000000) / 1000000) > 0)
			ft_print("1000000");
	}
	if (a > 999)
	{
		dec(((a % 1000000) / 1000));
		if (((a % 1000000) / 1000) > 0)
			ft_print("1000");
	}
	dec(a % 1000);
}
