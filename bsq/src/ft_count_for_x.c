#include "bsq.h"

int	ft_count_for_x(char *str)
{
	int t;
	int i;

	i = 0;
	t = 0;
	while (str[i] != '\n')
		i++;
	i++;
	while (str[i] != '\n')
	{
		i++;
		t++;
	}
	return (t);
}
