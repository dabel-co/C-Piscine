#include "bsq.h"

int	ft_atoi(char *str)
{
	int	i;
	int atoi;
	int n;

	i = 0;
	atoi = 0;
	n = 1;
	while (str[i] >= '0' && str[i] <= '9')
	{
		atoi = atoi * 10 + (str[i] - '0');
		i++;
	}
	atoi = atoi * n;
	return (atoi);
}
