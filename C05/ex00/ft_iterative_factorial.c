int	ft_iterative_factorial(int nb)
{
	int i;
	int c;

	i = 1;
	c = 1;
	if (nb == 0)
		return (1);
	if (nb < 0)
		return (0);
	while (i < nb)
	{
		c = c * (i + 1);
		i++;
	}
	return (c);
}
